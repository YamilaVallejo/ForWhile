
package forwhile;

/**
 *
 * @author Yamila
 */
public class ForWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       for (int i= 1; i<=10; ++i){
           System.out.println("FOR Numeros "+i);
       }
       int j=1;
       while (j<=10){
           System.out.println("WHILE Numeros: " + j);
           j++;
       }
    }
    
}
